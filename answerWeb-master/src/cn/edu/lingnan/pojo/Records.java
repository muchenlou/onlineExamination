package cn.edu.lingnan.pojo;

public class Records {
    private Integer recordsno;

    private Integer userno;

    private Integer typeno;

    private Integer acnumber;

    private Integer erunmber;

    private Integer wechatuserno;
    
    private String typename;

    public Integer getRecordsno() {
        return recordsno;
    }

    public void setRecordsno(Integer recordsno) {
        this.recordsno = recordsno;
    }

    public Integer getUserno() {
        return userno;
    }

    public void setUserno(Integer userno) {
        this.userno = userno;
    }

    public Integer getTypeno() {
        return typeno;
    }

    public void setTypeno(Integer typeno) {
        this.typeno = typeno;
    }

    public Integer getAcnumber() {
        return acnumber;
    }

    public void setAcnumber(Integer acnumber) {
        this.acnumber = acnumber;
    }

    public Integer getErunmber() {
        return erunmber;
    }

    public void setErunmber(Integer erunmber) {
        this.erunmber = erunmber;
    }

    public Integer getWechatuserno() {
        return wechatuserno;
    }

    public void setWechatuserno(Integer wechatuserno) {
        this.wechatuserno = wechatuserno;
    }

	public String getTypename() {
		return typename;
	}

	public void setTypename(String typename) {
		this.typename = typename;
	}
    
    
}